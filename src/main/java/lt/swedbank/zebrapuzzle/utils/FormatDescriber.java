package lt.swedbank.zebrapuzzle.utils;

public class FormatDescriber {
	private String format;
	private String file;

	public FormatDescriber(String format, String file) {
		this.format = format;
		this.file = file;
	}

	public String getFormat() {
		return format;
	}

	public String getFile() {
		return file;
	}

}
